<h3><a href="http://www.beeline-game.com/beeline-gets-a-taste-of-urban-beekeeping/">Beeline Gets a Taste of Urban Beekeeping</a></h3>

<div id="latest-post"><p>On Monday, August 22nd, I had the amazing experience of visiting an urban beekeeper in Astoria, Queens. For those who aren&#8217;t sure what that is, it&#8217;s exactly what it sounds like: the practice of keeping bees in an urban environment (<em>Thank you for the official definition, Wikipedia!</em>). <strong>Fun Fact:</strong> Beekeeping was illegal in NYC because health officials considered bees to be dangerous, but the ban was lifted in 2010 and <a href="http://www.wnyc.org/story/nycs-beekeeping-scene-buzzing/">now it&#8217;s thriving</a>!</p>
<figure id="attachment_37" style="width: 300px" class="wp-caption aligncenter"><img class="size-medium wp-image-37" src="http://www.beeline-game.com/wp-content/uploads/2016/09/beekeeper-kim-300x225.jpg" alt="Beeline Co-Creator Kim visits an urban beekeeper in Astoria" width="300" height="225" srcset="http://www.beeline-game.com/wp-content/uploads/2016/09/beekeeper-kim-300x225.jpg 300w, http://www.beeline-game.com/wp-content/uploads/2016/09/beekeeper-kim-768x576.jpg 768w, http://www.beeline-game.com/wp-content/uploads/2016/09/beekeeper-kim-1024x768.jpg 1024w, http://www.beeline-game.com/wp-content/uploads/2016/09/beekeeper-kim-1200x900.jpg 1200w" sizes="(max-width: 300px) 100vw, 300px" /><figcaption class="wp-caption-text">Beeline co-creator Kim visits urban beekeeper in Astoria</figcaption></figure>
<p>Fast forward to July 2016, I was doing some outreach for local beekeepers because (a) it&#8217;s really interesting and (b) I wanted to do some extra research for our board game, <a href="http://www.beeline-game.com">Beeline</a>. On this particular Monday morning, <a href="https://www.facebook.com/nycbeekeeper/?hc_ref=SEARCH&amp;fref=nf">The NYC Beekeeper</a> (his real name is Nick and he&#8217;s super cool) suggested that I tag along as he and fellow beekeeper, Jesse, inspected their hives that evening. They suspected one of their hives to be queenless (aka..<em> not good!</em> Check out <a href="http://beekeepinglikeagirl.com/signs-your-colony-is-queenless/">Signs Your Colony is Queenless</a>) and wanted to do a thorough inspection to make sure she was definitely gone. How exciting.. This is what bee nerd dreams are made of! With waivers agreed upon and safety gear donned, I climbed the one-story ladder behind Nick and Jesse and the inspection began.</p>
<figure id="attachment_40" style="width: 225px" class="wp-caption aligncenter"><img class="wp-image-40 size-medium" src="http://www.beeline-game.com/wp-content/uploads/2016/09/IMG_7719-1-e1473018970103-225x300.jpg" alt="Flow Hive" width="225" height="300" srcset="http://www.beeline-game.com/wp-content/uploads/2016/09/IMG_7719-1-e1473018970103-225x300.jpg 225w, http://www.beeline-game.com/wp-content/uploads/2016/09/IMG_7719-1-e1473018970103-768x1024.jpg 768w, http://www.beeline-game.com/wp-content/uploads/2016/09/IMG_7719-1-e1473018970103-1200x1600.jpg 1200w" sizes="(max-width: 225px) 100vw, 225px" /><figcaption class="wp-caption-text">Flow™ Hive</figcaption></figure>
<p>The whole process was fascinating. I wasn&#8217;t sure what to expect up there on that roof. I saw the wooden Flow™ Hives, lots of piping, and smoke. Jesse had lit some paper on fire in order to light the bee smoker, which I learned is used to calm the bees (<em>I always wondered what that was for</em>). Honeybees use pheromones as a means of communication within the hive. The smoke masks bee pheromones, confusing the bees but keeping them calm in order for beekeepers to work without any fear of attack. Jesse and Nick patiently took out row after row of the hive looking for any signs of queens (mainly larvae, as queens are the only bees that can lay fertile eggs). Gratefully, Nick gave me a play by play, let me among the swarm for the queen, and pointed out differences between the drones (male bees) and worker bees. We saw new &#8220;baby bees&#8221; emerging and some larvae &#8211; which left us utterly perplexed since the queen was still MIA.</p>
<figure id="attachment_38" style="width: 300px" class="wp-caption aligncenter"><img class="size-medium wp-image-38" src="http://www.beeline-game.com/wp-content/uploads/2016/09/IMG_7714-300x225.jpg" alt="Jesse and Nick carefully inspect the rows of hives" width="300" height="225" srcset="http://www.beeline-game.com/wp-content/uploads/2016/09/IMG_7714-300x225.jpg 300w, http://www.beeline-game.com/wp-content/uploads/2016/09/IMG_7714-768x576.jpg 768w, http://www.beeline-game.com/wp-content/uploads/2016/09/IMG_7714-1024x768.jpg 1024w, http://www.beeline-game.com/wp-content/uploads/2016/09/IMG_7714-1200x900.jpg 1200w" sizes="(max-width: 300px) 100vw, 300px" /><figcaption class="wp-caption-text">Jesse and Nick carefully inspect the hive</figcaption></figure>
<p>We searched and searched (<em>beekeeping requires patience..</em>) until Jesse called out that he&#8217;d <strong>spotted the queen</strong>! There she was, a thing of beauty &#8211; long and regal &#8211; there among her fellow bees. She had an identifying mark, a small white dot on her thorax, which is common practice in beekeeping. There&#8217;s a color code system which indicates when a queen was introduced into a hive. White, in this instance, means that she was introduced in a year ending in 1 or 6. The team was thrilled, although they had already ordered a new queen as a precaution that was scheduled to arrive the next day (<em>see previous blog post link above for why you don&#8217;t want a queenless hive</em>). Nonetheless, the relief was palpable&#8230; and I couldn&#8217;t help but declare myself the good luck charm!</p>
<p>Overall, this experience was a fun one! When I posted my picture on Facebook, I received several comments from people who say they would never do that&#8230; and maybe I wouldn&#8217;t have a few years ago. But I&#8217;m proud that I did, and I hope to have this experience again. I&#8217;ve made new beekeeper friends and that&#8217;s pretty sweet. I wouldn&#8217;t say that I was brave for doing this, I&#8217;m passionate and I want real-world experiences. One more chapter added to my book called life..</p>
<p>Oh, one more fun fact: If you ever find yourself near a beehive and smell bananas, RUN! Bees&#8217; attack phermone resembles the scent of a banana.</p>
